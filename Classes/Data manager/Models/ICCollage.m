#import "ICCollage.h"


@implementation ICCollage

@synthesize backgroundPath = mBackgroundPath;

- (id) init
{
	self = [super init];
	if (self != nil) {
		self.backgroundPath = @"UNDEFINED";
	}
	return self;
}

- (void) dealloc
{
	[mBackgroundPath release];
	[super dealloc];
}
@end
